                    <p align="justify"><span>Tim Debevec, rojen 12.10.1986, diplomant in magister Fakultete za varnostne vede ter gasilec prostovoljnega gasilskega društva Trnovo. V svojih mladih letih se je preizkusil v kar nekaj športih, največ časa pa je posvetil tenisu in borilnim veščinam. Je wellness inštruktor in ima licenco - Trener tenisa D. Na svoji športni poti se je preizkusil tudi v Rugby-u, kjer je nastopil za mladinsko reprezentanco. Svojo teniško pot je tako kot sestra Hana začel v teniškem klubu HIT, kasneje pa je treniral tudi na ŽTK Ljubljana, zdajšnjem TC Ljubljana. Zaradi mnogih poškodb, je spoznal pomembnost telesne priprave športnika, ter preventivnih postopkov v športu, s čimer se ljubiteljsko ukvarja. Ljubezen do športa je bila tako "kriva", da je prevzel mesto koordinatorja za šport na Fakulteti za varnostne vede, pod okriljem katerega je bil tudi eden od organizatorjev odhoda ekipe FVV na največji študentski štafetni tek imenovan Batatek. V želji, da svoje znanje prenaša naprej, je eden od ustanovnih članov ŠD Hannah.</span></p>
