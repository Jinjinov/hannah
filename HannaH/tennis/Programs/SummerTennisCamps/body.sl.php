                    <h2 class="item-page-title">
                        Poletni teniški tabor Ljubljana
                    </h2>
                    <p><span><strong>Termini:</strong></span></p>
                    <p><span>25. 7. - 29. 7. 2016</span></p>
                    <p><span>15. 8. - 19. 8. 2016 (samo za tekmovalce oz. dobre igralce***)<br></span></p>
                    <p><span>22.8. - 26.8. 2016<br></span></p>
                    <p><span><strong><span>Okvirni dnevni urnik poletnega teniškega tabora:</span></strong></span></p>
