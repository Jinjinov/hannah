                    <p><span>&nbsp;*** V tednu, ki je namenjen tekmovalcem, je v popoldanskem delu, ki je namenjen spoznavanju novih športov, predviden še en teniški trening. <br></span></p>
                    <p style="text-align: justify;"><span>V dopoldanskem času se bo izvajal intenzivni začetni in nadaljevalni tečaj tenisa, naučili pa se bomo tudi najosnovnejša in najpomembnejša pravila teniške igre. </span></p>
                    <p style="text-align: justify;"><span>Po kosilu se bomo vsak dan preizkusili v drugačnem športu ali celo dveh (baseball, kriket, cirkuške norčije, golf, skate norije, borilne veščine, vodne vragolije itd.), možno pa bo tudi v popoldanskem času ostati na teniškem igrišču in vaditi tehniko naučenih udarcev. </span></p>
                    <p><span>Cena tabora je 130 EUR / teden. Članom društva in drugemu, ter vsakemu naslednjemu otroku v družini nudimo popust in je tako cena 120 EUR / teden.</span></p>
                    <p style="text-decoration: underline;"><span>POMEMBNO: Po želji organiziran prevoz iz Ljubljane do Dola in popoldan spet nazaj do Ljubljane.&nbsp;</span></p>
                    <p><span><strong>OPOMBA</strong></span><span>: Program je namenjen predvsem osnovnošolcem, vendar pa ste po dogovoru vljudno vabljeni tako mlajši kot starejši! Lahko pa se udeležite le dopoldanskega - teniškega dela tabora (75 EUR)! </span></p>
